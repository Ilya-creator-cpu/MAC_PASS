public class Load {
    private int load;
    private int time;

    public Load(int load, int time){
        this.load = load;
        this.time = time;
    }

    public Load() {

    }

    public int getLoad() {
        return load;
    }

    public void setLoad(int load) {
        this.load = load;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}

